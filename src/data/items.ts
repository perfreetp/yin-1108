function buildItemDetail(base: ServiceItem, overrides: Partial<ServiceItemDetail> = {}): ServiceItemDetail {
  return {
    ...base,
    basicInfo: {
      serviceType: base.category.includes('登记') || base.code.startsWith('QR') ? '行政确认' : base.code.startsWith('JF') ? '行政给付' : base.code.startsWith('JL') ? '行政奖励' : base.code.startsWith('CJ') ? '行政裁决' : '行政许可',
      serviceObject: '公民、法人或其他组织',
      serviceMode: ['网上办理', '窗口办理'],
      legalBasis: [
        '《中华人民共和国行政许可法》相关条款',
        '《' + base.department + '管理条例》相关规定',
      ],
      handlingBasis: '根据相关法律法规规定办理',
      consultationPhone: '020-12345' + base.id.slice(-2),
      complaintPhone: '020-87654' + base.id.slice(-2),
      handlingLocation: base.department + '政务服务窗口',
      handlingTime: '工作日 上午9:00-12:00，下午13:30-17:00',
      ...(overrides.basicInfo || {}),
    },
    conditions: overrides.conditions || [],
    materials: overrides.materials || [],
    process: overrides.process || [],
    timeLimit: {
      legalDays: 20,
      promiseDays: 8,
      remark: '',
      ...(overrides.timeLimit || {}),
    },
    fee: {
      charge: false,
      standard: '不收费',
      basis: '',
      items: [],
      ...(overrides.fee || {}),
    },
    scenarios: overrides.scenarios || [],
    reviewRecords: overrides.reviewRecords || [],
    versions: overrides.versions || [],
  };
}

function buildConditions(prefix: string, texts: string[]) {
  return texts.map((text, i) => ({
    id: `${prefix}-cond-${i + 1}`,
    content: text,
    sort: i + 1,
    required: true,
    relatedProcessSteps: [`${prefix}-step-${Math.ceil((i + 1) / 2)}`],
  }));
}

function buildMaterials(prefix: string, specs: Array<{ name: string; remark?: string; type?: any; necessity?: any }>) {
  return specs.map((s, i) => ({
    id: `${prefix}-mat-${i + 1}`,
    name: s.name,
    type: s.type || 'both',
    count: 1,
    necessity: s.necessity || 'required',
    form: 'both' as const,
    source: 'applicant' as const,
    remark: s.remark || '',
    isBlankForm: true,
    isSample: i % 2 === 0,
    sort: i + 1,
    relatedScenarios: [`${prefix}-scenario-1`, `${prefix}-scenario-2`],
  }));
}

function buildProcess(prefix: string, steps: string[], dept: string) {
  return steps.map((name, i) => ({
    id: `${prefix}-step-${i + 1}`,
    step: i + 1,
    name,
    handler: '办理人员' + (i + 1),
    handlerDept: dept,
    duration: Math.max(1, Math.floor(8 / steps.length)),
    description: '办理本环节的相关事务并作出相应处理',
    conditions: [],
    relatedConditions: [],
  }));
}

function buildVersions(prefix: string, count: number, startVersion: string, statuses: string[]) {
  const vers: ServiceItemDetail['versions'] = [];
  const [maj, min] = startVersion.replace('v', '').split('.').map(Number);
  for (let i = count - 1; i >= 0; i--) {
    const v = `v${maj}.${min + i}`;
    const status = statuses[i] || (i === 0 ? 'published' : 'superseded');
    vers.push({
      id: `${prefix}-ver-${count - i}`,
      version: v,
      status: status as any,
      publishDate: status === 'draft' ? undefined : '2025-03-' + String(15 + count - i).padStart(2, '0'),
      createdBy: '编制人员',
      createdAt: '2025-03-' + String(10 + count - i).padStart(2, '0') + ' 10:00:00',
      changes: i === count - 1 ? ['初始版本发布'] : ['优化编制内容', '修正相关要素信息'],
    });
  }
  return vers;
}

